package zuoye;

public class Calculate {
	
	public static double jia( double a, double b)
	{
		return a+b;
	}
	
	public static double jian( double a, double b)
	{
		return a-b;
	}
	
	public static double cheng( double a, double b)
	{
		return a*b;
	}
	
	public static double chu( double a, double b)
	{
		return a/b;
	}
	
	
}
